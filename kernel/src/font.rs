//! Minimal 8x8 bitmap font.
//!
//! Only the glyphs used by the splash screen are defined. Each byte is one
//! row; the most-significant bit is the leftmost pixel.

pub fn glyph(ch: u8) -> [u8; 8] {
    match ch {
        b'A' => [0x18, 0x3C, 0x66, 0x66, 0x7E, 0x66, 0x66, 0x00],
        b'O' => [0x3C, 0x66, 0x66, 0x66, 0x66, 0x66, 0x3C, 0x00],
        b'S' => [0x3E, 0x60, 0x60, 0x3C, 0x06, 0x06, 0x7C, 0x00],
        b'a' => [0x00, 0x00, 0x3C, 0x06, 0x3E, 0x66, 0x3E, 0x00],
        b'h' => [0x60, 0x60, 0x7C, 0x66, 0x66, 0x66, 0x66, 0x00],
        b'l' => [0x18, 0x18, 0x18, 0x18, 0x18, 0x18, 0x1C, 0x00],
        b'o' => [0x00, 0x00, 0x3C, 0x66, 0x66, 0x66, 0x3C, 0x00],
        _ => [0x00; 8],
    }
}
